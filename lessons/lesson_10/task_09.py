# Определить в каком диапазоне находится число
# меньше 10
# от 10 до 20
# от 20 до 30
# от 30 до 40
# равно или больше 40

number = 12

if number < 10:
    print("Число меньше 10")
elif number < 20:
    print("Число от 10 до 20")
elif number < 30:
    print("Число от 20 до 30")
elif number < 40:
    print("Число от 30 до 40")
else:
    print("Число равно или больше 40")